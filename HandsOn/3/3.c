#include <stdio.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <fcntl.h>

int main(){
    int fd = creat("File.txt",S_IRWXG|S_IRWXU|S_IRWXO);
    if(fd<0){
        perror("failed to open");
    }
    printf("fd= %d\n",fd);
}

